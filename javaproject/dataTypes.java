public class dataTypes {
    public static void main(String[] args) {
        //Primitive Data Types :
        //Basic data types that stores single values.
        //They are not objects and do not have methods

        byte byteInt; //signed integer 8-bit
        short shortInt; //signed integer 16-bit
        int number; // signed integer 32-bit
        long longInt; // signed integer 64-bit
        float floatNumber; //decimal values with less precision 32-bit
        double doubleNumber; // decimal values with high precision 64-bits
        char String; //unicode characters 16-bit
        boolean bool; // boolean representation of true or false

        //Reference Data types
        //Classes : custom defined data types created using the `class` keyword
        class CustomDataType{
            String name;
            int age;
        }
    }
}
